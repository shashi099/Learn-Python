# 1️⃣ User से दो numbers लो और उनका sum print करो
num1 = int(input("Enter 1st num"))
num2 = int(input("Enter 2nd num"))
print("sum = ", num1+num2)


# 2️⃣ User से age लो और check करो age > 18 है या नहीं
age = int(input())
print(age > 18)

# 3️⃣ दो variables compare करके True/False print करो
var1 = 12
var2 = 21
print(var1 == var2)
print(var1 > var2)
print(var1 < var2)
print(var1 >= var2)
print(var1 <= var2)
print(var1 != var2)

# 4️⃣ 3 numbers input लेकर उनका average print करो
num1 = int(input("Enter 1st num"))
num2 = int(input("Enter 2nd num"))
num3 = int(input("Enter 3rd num"))
print("avg = ", (num2+num1+num3)/3)

